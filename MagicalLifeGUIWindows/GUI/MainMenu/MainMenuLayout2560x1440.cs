namespace MagicalLifeGUIWindows.GUI.MainMenu
{
    /// <summary>
    /// Holds some constants used to render the main menu when at the 1920 by 1080 resolution.
    /// </summary>
    public static class MainMenuLayout2560x1440
    {
        /// <summary>
        /// The x position at which the left part of the buttons on the main menu begin.
        /// </summary>
        public static readonly int ButtonX = 20;

        /// <summary>
        /// How wide the buttons are.
        /// </summary>
        public static readonly int ButtonWidth = 200;

        /// <summary>
        /// How tall the buttons are.
        /// </summary>
        public static readonly int ButtonHeight = 50;

        /// <summary>
        /// The y position of the top of the new game button.
        /// </summary>
        public static readonly int NewGameButtonY = 100;

        /// <summary>
        /// The y position of the top of the host game button.
        /// </summary>
        public static readonly int LoadGameButtonY = 200;

        /// <summary>
        /// The y position of the top of the join game button.
        /// </summary>
        public static readonly int JoinGameButtonY = 300;

        /// <summary>
        /// The y position of the top of the quit button.
        /// </summary>
        public static readonly int QuitButtonY = 400;
    }
}